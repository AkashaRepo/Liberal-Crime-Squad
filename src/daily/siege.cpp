int fooddaysleft(int loc)
{
	int eaters = numbereating(loc);
	if (eaters == 0) return -1;
	return LocationsPool::getInstance().getStoresAmount(loc) / eaters + ((LocationsPool::getInstance().getStoresAmount(loc)%eaters) > eaters / 2);
}
